#Set radius to be 2
#diameter of a circle is twice its radius
#use given value of pie in question
#formula to find circumference
#formula to find area
#print area, circumference, and diameter as strings.

radius = 2    
diameter = radius * 2    
pi = 3.14159            
circumference = 2 * pi * radius      
area = pi * radius ** 2           

print("The diameter of the circle is " + str(diameter))
print("The circumference of the circle is " + str(circumference))
print("The area of the circle is " + str(area))

