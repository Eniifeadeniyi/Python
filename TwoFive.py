radius = 2    #Set radius to be 2
diameter = radius * 2    #diameter of a circle is twice its radius
pie = 3.14159             #use given value of pie in question
circumference = 2 * pie * radius      #formula to find circumference
area = pie * radius ** 2           #formula to find area

print("The diameter of the circle is " + str(diameter))
print("The circumference of the circle is " + str(circumference))
print("The area of the circle is " + str(area))
